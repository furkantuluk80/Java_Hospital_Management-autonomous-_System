    boolean desenKontrol(String desenMetni, String icindeDesenArananMetin) {  // Karakter Kontrolü yapmak için
        Pattern desen = Pattern.compile(desenMetni);
        Matcher arama = desen.matcher(icindeDesenArananMetin);
        return arama.find();

    }

    boolean tcNumarasınınAynısındanVarMı() {    //Tc numaralının kontrolünü yapmak için
        boolean aynıMı = false;
        for (Resepsiyon r : ResepsiyonlariGoruntule.list) {
            if (r.getTc().equals(jTextFieldTC.getText())) {
                aynıMı = true;
            }
        }
        return aynıMı;
    }

