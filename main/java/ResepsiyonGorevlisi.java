    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed

        if (jTextFieldAd.getText().isEmpty() || desenKontrol("[^A-z\\s]", jTextFieldAd.getText())) {                              // Ad sadece harflerden
            JOptionPane.showMessageDialog(this, "Lütfen adınızı düzgün giriniz!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (jTextFieldSoyad.getText().isEmpty() || desenKontrol("[^A-z\\s]", jTextFieldSoyad.getText())) {                 // soyad sadece harflerden
            JOptionPane.showMessageDialog(this, "Lütfen soyadınızı düzgün giriniz!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (jTextFieldTC.getText().isEmpty() || desenKontrol("[^0-9]", jTextFieldTC.getText()) || jTextFieldTC.getText().length() != 11) {    // TC 11 hane ve rakam
            JOptionPane.showMessageDialog(this, "Lütfen tc no'da sadece rakam kullanınız!\nAyrıca 11 basamaklı olmalı!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (jTextFieldTel.getText().isEmpty() || jTextFieldTel.getText().length() != 10 || desenKontrol("[^0-9]", jTextFieldTel.getText())) { // Telefon 10 hane ve rakam
            JOptionPane.showMessageDialog(this, "Telefon numarası 10 basamaklı ve sadece rakamdan oluşmalıdır!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (jTextFieldYaş.getText().isEmpty() || Integer.parseInt(jTextFieldYaş.getText()) < 18) {                                              // Yaş 18den büyük 
            JOptionPane.showMessageDialog(this, "18 yaşından küçükler hesap oluşturamazlar!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (jTextFieldEmail.getText().isEmpty() || !desenKontrol("[A-Za-z0-9]+[@][A-Za-z0-9]+(.com)", jTextFieldEmail.getText())) {              //maile uygun format
            JOptionPane.showMessageDialog(this, "Lütfen emaili düzgün bir şekilde giriniz!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            return;
        }  else if (tcNumarasınınAynısındanVarMı()) {                                                                                               
            JOptionPane.showMessageDialog(this, "TC numaranız veri tabanındaki birisi ile uyuşuyor.\nLütfen doğru girdiğinizden emin olunuz!", "Hata!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String cinsiyet = "";
        if (jRadioButtonErkek.isSelected()) {
            cinsiyet = "erkek";
        } else if (jRadioButtonKadın.isSelected()) {
            cinsiyet = "kadın";
        }

        

        String[] seçimler = {"Evet", "Hayır"};
        int seçim = JOptionPane.showOptionDialog(rootPane, "Bu bilgilerinizle kaydolmak istediğinize emin misiniz?", "Uyarı!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, seçimler, seçimler[0]);

        if (seçim == 0) {
            Resepsiyon r = new Resepsiyon(jTextFieldAd.getText(), jTextFieldSoyad.getText(), jTextFieldEmail.getText(), cinsiyet,
                    jTextFieldTel.getText(), jTextFieldTC.getText(), Integer.parseInt(jTextFieldYaş.getText()), kullaniciadi.getText(), sifre.getText());

     
            
            
            JOptionPane.showMessageDialog(this, "İşleminiz başarıyla tanımlanmıştır!", "Tebrikler!", JOptionPane.INFORMATION_MESSAGE);

            System.out.println("kullanıcılar: " + ResepsiyonlariGoruntule.list);
            System.out.println(r.getAd() + " " + r.getSoyad() + " " + r.getKullanıcıAdı() + " " + r.getŞifre());
                   rg.list.add(r);
                   
            Object rowData[] = new Object[9];
            
            for (int i = 0; i < rg.list.size(); i++) {
                rg.list.get(i).getAd();
                rg.list.get(i).getSoyad();
                rg.list.get(i).getEmail();
                rg.list.get(i).getCinsiyet();
                rg.list.get(i).getTel();
                rg.list.get(i).getTc();
                rg.list.get(i).getYaş();
                rg.list.get(i).getKullanıcıAdı();
                rg.list.get(i).getŞifre();
                
                
                rg.dtm.addRow(rowData);
                
                
            }
            
            
            return;
        } else if (seçim == 1) {
            JOptionPane.showMessageDialog(this, "İşleminiz iptal edilmiştir!", "Uyarı", JOptionPane.INFORMATION_MESSAGE);
            rg.dtm.addRow(new Object[]{jTextFieldAd.getText(), jTextFieldSoyad.getText(), jTextFieldEmail.getText(), cinsiyet,
                jTextFieldTel.getText(), jTextFieldTC.getText(), Integer.parseInt(jTextFieldYaş.getText()), kullaniciadi.getText(), sifre.getText()});
        }


    }//GEN-LAST:event_ekleActionPerformed

