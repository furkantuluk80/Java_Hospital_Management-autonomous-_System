    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        //Tabledeki verileri güncelleme
        int secilisatir = table.getSelectedRow();

        try {
            String tc1 = tc.getText();
            String isim1 = hastaismi.getText();
            String adres1 = adres.getText();
            String cinsiyet1="";
            if (erkek.isSelected()) {
                cinsiyet1 = erkek.getText();
            } else if (kadın.isSelected()) {
                cinsiyet1 = kadın.getText();
            }
            String hastalik1 = hastalık.getText();
            String doktoradi1 = boxdoktor.getSelectedItem().toString();
            String uzman1 = jboxalan.getSelectedItem().toString();
            String odano1 = box.getSelectedItem().toString();
            String yatakno1 = box2.getSelectedItem().toString();
            String tesis1 = box3.getSelectedItem().toString();

            dtm.setValueAt(tc1, secilisatir, 0);
            dtm.setValueAt(isim1, secilisatir, 1);
            dtm.setValueAt(adres1, secilisatir, 2);
            dtm.setValueAt(cinsiyet1, secilisatir, 3);
            dtm.setValueAt(hastalik1, secilisatir, 4);
            dtm.setValueAt(uzman1, secilisatir, 5);
            dtm.setValueAt(doktoradi1, secilisatir, 6);
            dtm.setValueAt(odano1, secilisatir, 7);
            dtm.setValueAt(yatakno1, secilisatir, 8);
            dtm.setValueAt(tesis1, secilisatir, 9);

            JOptionPane.showMessageDialog(this, "Başarıyla Güncellendi");

            if (table.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table boş");
            }
        } catch (NullPointerException e) {

        }
    }//GEN-LAST:event_guncelleActionPerformed

    private void kaldırActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaldırActionPerformed
        // Tabledeki verilerin hepsini kaldırma

        try {
            if (table.getSelectedRow() == -1) {
                return;
            }
            String[] seçimler = {"Evet", "Hayır"};
            int seçim = JOptionPane.showOptionDialog(this, "Bu hastayı silmek istediğinize emin misiniz!", "Uyarı!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE, null, seçimler, seçimler[0]);

            if (seçim == 0) {
                dtm.removeRow(table.getSelectedRow());
                  
                for (int i = 0; i < table.getRowCount(); i++) {
                    String tcno= (String)table.getModel().getValueAt(i, 0);
                    for(Hastalar h : list){
                        if(tcno.equals(h.getTc())){
                            list.remove(h);
                        }
                        
                        
                    }
                    
                }

                JOptionPane.showMessageDialog(this, "Hasta silinmiştir!", "Uyarı!", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Silmek istediğiniz hastayı seçiniz!", "Uyarı!", JOptionPane.WARNING_MESSAGE);

            }

        } catch (RuntimeException e) {

        }

    }//GEN-LAST:event_kaldırActionPerformed

