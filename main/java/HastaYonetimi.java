    private void kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydetActionPerformed
        String cinsiyet = "";
        String mesaj = "Bu yatak doludur .\nLütfen başka bir oda veya yatak seçiniz. \nEğer tüm oda ve yataklar doluysa başka bloğa bakınız!";
        if (erkek.isSelected()) {
            cinsiyet = erkek.getText();
        } else if (kadın.isSelected()) {
            cinsiyet = kadın.getText();
        }

        try {
            if (tcNumarasınınAynısındanVarMı()) {   // TC numara kontrolü
                JOptionPane.showMessageDialog(this, "TC numaranız veri tabanındaki birisi ile uyuşuyor.\nLütfen doğru girdiğinizden emin olunuz!", "Hata!", JOptionPane.WARNING_MESSAGE);
                return;
            } else {

                for (int i = 0; i < table.getRowCount(); i++) {     // Tabledeki oda yatak blok yerlerine bakarak aynısı var mı yok mu onun kontrolü eğer yoksa eklesin.
                    if (box.getSelectedItem().equals(table.getModel().getValueAt(i, 7)) && box2.getSelectedItem().equals(table.getModel().getValueAt(i, 8)) && box3.getSelectedItem().equals(table.getModel().getValueAt(i, 9))) {
                        JOptionPane.showMessageDialog(this, mesaj, "Uyarı!", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                }

                dtm.addRow(new Object[]{tc.getText(), hastaismi.getText(), adres.getText(), cinsiyet, hastalık.getText(), // Table ve Liste ekleme verileri.
                    boxdoktor.getSelectedItem(), jboxalan.getSelectedItem(), box.getSelectedItem(), box2.getSelectedItem(), box3.getSelectedItem()});
                Hastalar h = new Hastalar(tc.getText(), hastaismi.getText(), adres.getText(), cinsiyet, hastalık.getText(), boxdoktor.getSelectedItem().toString(), jboxalan.getSelectedItem().toString(), box.getSelectedItem().toString(), box2.getSelectedItem().toString(), box3.getSelectedItem().toString());
                list.add(h);

                JOptionPane.showMessageDialog(this, "Yeni Hasta Başarıyla Eklenmiştir.");
            }

        } catch (NullPointerException e) {

        } catch (NumberFormatException n) {

        }

    }//GEN-LAST:event_kaydetActionPerformed

